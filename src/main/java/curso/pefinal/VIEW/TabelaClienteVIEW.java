     private void listarClientes(){
        try{
            ClienteDAO cliente = new ClienteDAO();
            DefaultTableModel model = (DefaultTableModel) tbcliente.getModel();
            
            model.setNumRows(0);
            ArrayList<ClienteDTO> clientes = new ArrayList<>();
            clientes = cliente.listarCliente();
            
            for(int cont = 0; cont < clientes.size(); cont++){
                model.addRow(new Object[]{
                    clientes.get(cont).getId_cliente(),
                    clientes.get(cont).getNome(),
                    clientes.get(cont).getCpf(),
                    clientes.get(cont).getTelefone()
                        
                });
            }
            
        }catch(Exception erro){
            JOptionPane.showMessageDialog(null, "TabelaClienteVIEW: " + erro);
        }

    }
    
    private void alterarClientes(){
        
        String idInicial;
        int selecionar = tbcliente.getSelectedRow();
        DefaultTableModel model = (DefaultTableModel) tbcliente.getModel();
        idInicial = model.getValueAt(selecionar, 0).toString();
        int id = Integer.parseInt(idInicial);
                
        CadastroClienteVIEW cadCli = new CadastroClienteVIEW();
        cadCli.setVisible(true);
        cadCli.alterarClientes(id);
        
        dispose();
    }
    
    private void excluirClientes(){
        String idInicial;
        int selecionar = tbcliente.getSelectedRow();
        DefaultTableModel model = (DefaultTableModel) tbcliente.getModel();

        //retorna o valor no campo da linha selecionar e da coluna 0
        idInicial = model.getValueAt(selecionar, 0).toString();
        int id = Integer.parseInt(idInicial);
        try{

            TesteAgendamentoDTO teste = new TesteAgendamentoDTO();
            teste.setId(id);

            TesteAgendamentoDAO testeagen = new TesteAgendamentoDAO();

            ResultSet rsTesteDAO;
            rsTesteDAO = testeagen.testarAgendamentoCliente(teste);
            
            if(rsTesteDAO.next()){
                JOptionPane.showMessageDialog(null, "Não é possível fazer a exclusão\nO Cliente possui agendamento");
            }else{
                ClienteDAO cliente = new ClienteDAO();
            
                Object[] opcoes = { "Sim", "Não" }; 
                int resposta = JOptionPane.showOptionDialog(null, "Confirma a Exclusão?", "", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE, null, opcoes, opcoes[0]);
                if(resposta == 0){
                    cliente.excluirCliente(id);
                }
            }
            
            
        }catch(Exception erro){
            JOptionPane.showMessageDialog(null, "Excluir ClienteVIEW: " + erro);
        }
    }
